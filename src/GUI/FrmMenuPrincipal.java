    private void cambiarSeccion( int seccion ) {
        principal.remove(0);
        switch(seccion) {
            case 0 : {
                principal.add(pc);
                break;
            }
            case 1 : {
                principal.add(pa);
                break;
            }
            case 2 : {
                principal.add(pt);
                break;
            }
            case 3 : {
                principal.add(pf);
                break;
            } 
            case 4 : {
                principal.add(pu);
                break;
            }
            case 5 : {
                principal.add(pma);
                break;
            }
            case 6 : {
                principal.add(pmo);
                break;
            }
        }
        
        principal.revalidate();
    }
    
